			<div class="news-description" style="text-align: justify;">
							<p>Tugas Akhir (TA) adalah karya akademik dalam bentuk khusus untuk memenuhi persyaratan dalam memperoleh gelar akademik Sarjana Teknik. Tugas Akhir pada dasarnya merupakan kristalisasi hasil penelitian yang dilakukan mandiri dan dapat merefleksikan kemampuan penguasaan kerangka keilmuan, metode analisis, asas-asas penelitian serta nilai, etika dan norma akademik sejalan dengan disiplin ilmu yang ditempuhnya.</p>
<p>Tugas Akhir adalah manifestasi integritas dan sikap ilmiah dengan karakteristik sebagai berikut :</p>
<ol>
<li>Menghormati norma akademika dan norma etika akademika.</li>
<li>Jujur dalam hal merekam informasi, mengolah, menganalisa, menginterprestasikan dan mengambil kesimpulan berdasarkan alat ukur dan alat penguji yang lazim berlaku menurut metode ilmu dan tidak terpengaruh oleh desakan siapapun atau kepentingan pribadi.</li>
<li>Mampu mengenakan asas integritas dalam menilai sesuatu secara proporsional sehingga dapat memberikan kebijakan totalitas yang objektif.</li>
<li>Memiliki keterpaduan dan keserasian keseimbangan kematangan intelektual dan emosional, sehingga bersikap tenang dan tetap objektif dalam pandangannya.</li>
<li>Konsisten dalam sikap dan pandangan, dalam penggunaan istilah relevansinya pada seluruh sajian karya akademik, baik tertulis maupun lisan dalam suatu forum.</li>
</ol>
<p>Tugas akhir bertujuan untuk mengintegrasikan seluruh kemampuan mahasiswa yang telah diperoleh selama studi untuk menyelesaikan suatu permasalahan yang bobotnya memadai dan sifatnya komprehensif mencakup berbagai macam ilmu, dengan cara penyelesaian yang dapat dipertanggungjawabkan secara ilmiah. Permasalahan yang dijadikan topik bisa berupa suatu problematika yang pemecahannya akan memperbaiki perfomansi suatu sistem atau bagian-bagiannya atau suatu perumusan konsep/teori baru. </p>
<p><strong>Prosedur Penyusunan Tugas Akhir</strong></p>
<ol>
<li>Telah memenuhi seluruh persyaratan pengambilan Tugas Akhir.</li>
<li>Mahasiswa mendaftar ke koordinator Tugas Akhir masing-masing jurusan di lingkungan Fakultas Teknik UNSUR Cianjur dengan melampirkan Proposal Tugas Akhir.</li>
<li>Mahasiswa mendapatkan buku Panduan Tugas Akhir.</li>
<li>Koordinator Tugas Akhir mengalokasikan mahasiswa ke dosen pembimbing masing-masing jurusan sesuai dengan bidang Tugas Akhir yang dilakukan.</li>
<li>Tugas Akhir akan dibimbing oleh 1 (satu) atau 2 (dua)dosen pembimbing yang ditetapkan dengan SK Dekan berdasarkan usulan Ketua Jurusan.</li>
<li>Sebelum mahasiswa mengumpulkan data terlebih dahulu harus mendapat bimbingan dari dosen pembimbing.</li>
<li>Mahasiswa melakukan bimbingan dengan dosen pembimbing yang telah dialokasikan koordinator Tugas Akhir</li>
<li>Jika mahasiswa telah menyelesaikan minimal tahap analisis, atas persetujuan dari Dosen pembimbing, selanjutnya melapor ke koordinator Tugas Akhir untuk mengajukan seminar Tugas Akhir.</li>
<li>Mahasiswa memperbanyak materi Seminar Tugas akhir sebanyak 2 (dua) eksemplar untuk keperluan seminar.</li>
<li>Koordinator Tugas Akhir menjadwalkan seminar sesuai dengan kesepakatan antara dosen penguji dan koordinator Tugas Akhir.</li>
<li>Jumlah minimal peserta seminar Tugas Akhir ditentukan oleh jurusan masing-masing.</li>
<li>Dosen Pembimbing dan Dosen Penelaah menghadiri Seminar Tugas Akhir</li>
<li>Dosen Pembimbing menyerahkan berita acara dan nilai seminar ke koordinator Tugas Akhir, selanjutnya diproses dan menyerahkan nilai kepada tenaga administratif Jurusan.</li>
<li>Sidang Tugas Akhir bisa dilaksanakan apabila Mahasiswa telah menyelesaikan seluruh Laporan Tugas Akhir.</li>
<li>Mahasiswa menggandakan Laporan Tugas Akhir sebanyak 4 (empat) eksemplar.</li>
<li>Koordinator Tugas Akhir menjadwalkan Sidang Sesuai dengan kesepakatan antara Pembimbing dan Penguji.</li>
<li>Jumlah minimal yang menghadiri Sidang Tugas Akhir di tentukan oleh Jurusan masing-masing</li>
<li>Dosen Pembimbing dan Dosen Penguji menghadiriSidang Tugas Akhir</li>
<li>Dosen Pembimbing menyerahkan berita acara dan nilai Sidang ke koordinator Tugas Akhir, selanjutnya diproses dan menyerahkan nilai kepada tenaga administratif Jurusan.</li>
